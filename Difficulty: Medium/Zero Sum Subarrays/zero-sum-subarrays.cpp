class Solution {
  public:
    int findSubarray(vector<int> &arr) {
        // code here.
        int sum=0,ans=0;
        map<int,int> mpp;
        mpp[0]=1;
        for(int i=0;i<arr.size();i++){
            sum+=arr[i];
            ans+=mpp[sum];
            mpp[sum]++;
        }
        return ans;
    }
};

