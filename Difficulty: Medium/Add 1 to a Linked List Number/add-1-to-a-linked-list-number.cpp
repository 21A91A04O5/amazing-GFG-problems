class Solution {
  public:
    int fun(Node* t){
        if(t==NULL) return 1;
        int c=t->data+fun(t->next);
        t->data=c%10;
        return c/10;
    }
    Node* addOne(Node* head) {
        Node* t=new Node(1);
        if(fun(head)==1){ t->next=head; head=t; }
        return head;
    }
};

